a = 10
b = 3
hasil = a == b
print(a,'==',b,'=',hasil)

#operasi !=
hasil = a != b
print(a,'!=',b,'=',hasil)

#operasi >
hasil = a > b
print(a,'>',b,'=',hasil)

#operasi <
hasil = a < b
print(a,'<',b,'=',hasil)

#operasi >=
hasil = a >= b
print(a,'>=',b,'=',hasil)

#operasi <=
hasil = a <= b
print(a,'<=',b,'=',hasil)
